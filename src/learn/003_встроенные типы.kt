package learn

fun main() {
    val s: String = "строка"
    val c: Char = 'c' // символ
    val b: Boolean = true
    val i: Int = 1 // целочисленный
    val d: Double = 1.0
    val byte: Byte = 1
    val float: Float = 1.0f
    val long: Long = 1L
    val short: Short = 1
    val l: List<Int> = listOf(1, 2, 3) // список
    val set: Set<Int> = setOf(1, 2, 3) // множество
    val map: Map<String, Int> = mapOf("one" to 1, "two" to 2, "three" to 3) // ассоциативный массив
}